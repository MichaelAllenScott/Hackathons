package com.hackathon.healthtracker.model;

public class Task {

	private long id;
	private String description;
	
	public long getId() {
		return id;
	}
	public String getDescription() {
		return description;
	}
	public void setId(long id) {
		this.id = id;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	
}
